package telerikalpha.workshop.training2;

public class ArithmeticOperators {
    public static void main(String args[]) {
        int numberOne = 100;
        int numberTwo = 20;

        System.out.println("NumberOne + NumberTwo = " + (numberOne + numberTwo) );
        System.out.println("NumberOne - NumberTwo = " + (numberOne - numberTwo) );
        System.out.println("NumberOne * NumberTwo = " + (numberOne * numberTwo) );
        System.out.println("NumberOne / NumberTwo = " + (numberOne / numberTwo) );
        System.out.println("NumberOne % NumberTwo = " + (numberOne % numberTwo) );
    }
}