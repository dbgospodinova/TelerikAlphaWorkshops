package telerikalpha.workshop.training2;

public class AutoincrementAndDecrementOperators {
    public static void main(String[] args) {
        int numberOne = 100;
        int numberTwo = 200;
        numberOne++;
        numberTwo--;
        System.out.println("num1++ is: "+numberOne);
        System.out.println("num2-- is: "+numberTwo);
    }
}
